package com.chaipoint.helperclasses;

public class UserInfo {

	private String email;
	private String contact_primary;
	private String contact_secondary;
	private String customerId;
	private String name;
	private String name_prefix;

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getContact_primary() {
		return contact_primary;
	}

	public void setContact_primary(String contact_primary) {
		this.contact_primary = contact_primary;
	}

	public String getContact_secondary() {
		return contact_secondary;
	}

	public void setContact_secondary(String contact_secondary) {
		this.contact_secondary = contact_secondary;
	}

	public String getCustomerId() {
		return customerId;
	}

	public void setCustomerId(String customerId) {
		this.customerId = customerId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getName_prefix() {
		return name_prefix;
	}

	public void setName_prefix(String name_prefix) {
		this.name_prefix = name_prefix;
	}

}
