package com.chaipoint.helperclasses;

public class AddressInfo {

	private String name;
	private String phone;
	private String address_id;
	private String city;
	private String state;
	private String address_line_first;
	private String address_line_second;
	private String landmark;
	private String pinCode;
	private String address_type;
	private String latitude;
	private String longitude;

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getAddress_id() {
		return address_id;
	}

	public void setAddress_id(String address_id) {
		this.address_id = address_id;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public String getAddress_line_first() {
		return address_line_first;
	}

	public void setAddress_line_first(String address_line_first) {
		this.address_line_first = address_line_first;
	}

	public String getAddress_line_second() {
		return address_line_second;
	}

	public void setAddress_line_second(String address_line_second) {
		this.address_line_second = address_line_second;
	}

	public String getLandmark() {
		return landmark;
	}

	public void setLandmark(String landmark) {
		this.landmark = landmark;
	}

	public String getPinCode() {
		return pinCode;
	}

	public void setPinCode(String pinCode) {
		this.pinCode = pinCode;
	}

	public String getAddress_type() {
		return address_type;
	}

	public void setAddress_type(String address_type) {
		this.address_type = address_type;
	}

	public String getLatitude() {
		return latitude;
	}

	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}

	public String getLongitude() {
		return longitude;
	}

	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}

}
